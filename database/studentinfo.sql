create table studentinfo
(
	studentId varchar(50) not null
		primary key,
	studentName varchar(10) null,
	classId varchar(30) null,
	college varchar(30) null,
	studentType varchar(10) null,
	experience varchar(300) null,
	hobby1 varchar(10) null,
	hobby2 varchar(10) null,
	hobby3 varchar(10) null
);

